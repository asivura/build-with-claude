_CELL_W = 60
_CELL_H = 23
_COL_X = (0, 60, 120, 180)
_ROW_Y = (22, 45, 68, 91)

# Bitmap size and scale. 10x9 source @ scale 2 = 20x18 on screen,
# centered in the 60x23 cell with a comfortable margin so the cursor
# border doesn't kiss the icon.
_BMP_W = 10
_BMP_H = 9
_SCALE = 2
_ICON_PX_W = _BMP_W * _SCALE
_ICON_PX_H = _BMP_H * _SCALE

_START_LIVES = 3
_FLASH_MS = 220


def _cell_xy(idx):
    return _COL_X[idx % 4], _ROW_Y[idx // 4]


def _cell_center(idx):
    x, y = _cell_xy(idx)
    return x + _CELL_W // 2, y + _CELL_H // 2


def _set_font():
    try:
        _LCD.setFont(_LCD.FONTS.DejaVu9)
    except Exception as e:
        print("spot_diff: setFont fallback:", e)


# === Bitmap renderer =====================================================

def _draw_bitmap(cx, cy, bm, palette):
    """Render a small character bitmap centered at (cx, cy).

    `palette` maps each char in `bm` to an RGB color or to `None`
    (transparent — used for '.' background pixels). Adjacent same-char
    cells in a row are coalesced into one fillRect; with 16 icons at
    81 cells each, naive per-pixel rendering would issue 1300+ SPI
    writes per puzzle and visibly tear during the refresh."""
    ox = cx - _ICON_PX_W // 2
    oy = cy - _ICON_PX_H // 2
    for ry, row in enumerate(bm):
        rx = 0
        L = len(row)
        while rx < L:
            ch = row[rx]
            n = 1
            while rx + n < L and row[rx + n] == ch:
                n += 1
            color = palette.get(ch)
            if color is not None:
                _LCD.fillRect(
                    ox + rx * _SCALE,
                    oy + ry * _SCALE,
                    n * _SCALE,
                    _SCALE,
                    color,
                )
            rx += n


