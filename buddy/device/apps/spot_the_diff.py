"""spot_the_diff -- find the odd emoji in a 4x4 grid.

Each round paints 16 little pixel-art emojis. Fifteen of them are the
same; one is subtly different (mouth flipped, color swapped, mirrored
orientation, missing dot, etc.). Move the highlight with the arrow
keys and press SPACE on the odd one. Three lives, score goes up by 1
per correct pick.

### Controls

    , . ; /   move cursor (left / down / up / right)
    A S W D   alternate movement (gamer scheme)
    SPACE     pick the highlighted cell
    R         restart on the game-over screen
    `         exit back to the launcher

### Why bitmaps instead of real emojis

The DejaVu / Montserrat fonts shipped with this firmware don't have
emoji glyphs — drawing a face from a string would just show a tofu
box. Every "emoji" here is a 10x9 character bitmap rendered at 2x
scale (20x18 px on screen), painted with `fillRect`. Run-length
coalescing in `_draw_bitmap` cuts the per-icon SPI traffic to a few
dozen calls per row, which keeps the 16-cell refresh under a frame.

### Layout (240x135)

    Header:      y=0..19    DARK band, ORANGE hairline at y=20
    Play grid:   y=22..113  4x4 grid of 60x23 cells (contiguous)
    Hint strip:  y=117..134 DARK band, controls hint

Grid: x = col * 60, y = 22 + row * 23, each cell 60 wide x 23 tall.
"""

