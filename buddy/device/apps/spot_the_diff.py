# === Chrome / HUD ========================================================

def _draw_chrome():
    _LCD.fillScreen(_BLACK)
    _LCD.fillRect(0, 0, _W, 20, _DARK)
    _LCD.fillRect(0, 20, _W, 1, _ORANGE)
    _LCD.setTextSize(1)
    _LCD.setTextColor(_ORANGE, _DARK)
    _LCD.drawString("Spot the Odd One", 6, 5)
    _LCD.fillRect(0, _H - 18, _W, 18, _DARK)
    _LCD.setTextColor(_GRAY_MID, _DARK)
    hint = ",.;/ move  SPACE pick  ` exit"
    _LCD.drawString(hint, (_W - _LCD.textWidth(hint)) // 2, _H - 14)


def _update_header(score, lives):
    # Right portion only — repainting the whole header every tick
    # would flicker the title text.
    _LCD.fillRect(140, 0, _W - 140, 20, _DARK)
    _LCD.setTextSize(1)
    _LCD.setTextColor(_CREAM, _DARK)
    text = "score {}  lives {}".format(score, lives)
    x = _W - 6 - _LCD.textWidth(text)
    _LCD.drawString(text, x, 5)


# === Grid + cursor =======================================================

def _draw_cursor(idx, on):
    """Paint or erase the 1-px cell highlight. Cells are contiguous,
    so erasing with black plays nice with neighboring borders and
    doesn't bleed into the centered icon (~20 px horizontal margin
    on each side of the 60-wide cell)."""
    x, y = _cell_xy(idx)
    color = _ORANGE if on else _BLACK
    _LCD.fillRect(x, y, _CELL_W, 1, color)
    _LCD.fillRect(x, y + _CELL_H - 1, _CELL_W, 1, color)
    _LCD.fillRect(x, y, 1, _CELL_H, color)
    _LCD.fillRect(x + _CELL_W - 1, y, 1, _CELL_H, color)


def _draw_grid(puzzle, diff_idx, invert):
    normal, different, palette = puzzle
    bm_default = different if invert else normal
    bm_odd = normal if invert else different
    # Clear the body band (header + hairline above stay untouched).
    _LCD.fillRect(0, 21, _W, _H - 21 - 18, _BLACK)
    for i in range(16):
        cx, cy = _cell_center(i)
        bm = bm_odd if i == diff_idx else bm_default
        _draw_bitmap(cx, cy, bm, palette)


def _flash_cell(idx, color, ms):
    x, y = _cell_xy(idx)
    _LCD.fillRect(x, y, _CELL_W, _CELL_H, color)
    time.sleep_ms(ms)


def _restore_cell(idx, puzzle, diff_idx, invert):
    """Repaint a single cell from scratch — used after the red flash
    so the picked cell returns to its icon and the round can
    continue."""
    x, y = _cell_xy(idx)
    _LCD.fillRect(x, y, _CELL_W, _CELL_H, _BLACK)
    cx, cy = _cell_center(idx)
    normal, different, palette = puzzle
    bm_default = different if invert else normal
    bm_odd = normal if invert else different
    bm = bm_odd if idx == diff_idx else bm_default
    _draw_bitmap(cx, cy, bm, palette)


# === Input ===============================================================

def _key_intent(k):
    if k is None:
        return None
    if isinstance(k, int):
        if k in (0x1B, 0x08, 0x09, 0x7F, 0x03):
            return ("exit",)
        if k == 0x20:
            return ("pick",)
        if k in (0x0A, 0x0D):
            return ("pick",)
        if 0x20 < k <= 0x7E:
            k = chr(k)
        else:
            return None
    if not isinstance(k, str) or not k:
        return None
    ch = k.lower()
    if ch == " ":
        return ("pick",)
    if ch in ("`", "~"):
        return ("exit",)
    if ch in (",", "a"):
        return ("left",)
    if ch in ("/", "d"):
        return ("right",)
    if ch in (";", "w"):
        return ("up",)
    if ch in (".", "s"):
        return ("down",)
    if ch == "r":
        return ("restart",)
    return None


def _move_cursor(cur, intent):
    r = cur // 4
    c = cur % 4
    if intent == "left":
        c = (c - 1) % 4
    elif intent == "right":
        c = (c + 1) % 4
    elif intent == "up":
        r = (r - 1) % 4
    elif intent == "down":
        r = (r + 1) % 4
    return r * 4 + c


# === Game flow ===========================================================

def _play_puzzle(kb, score, lives):
    """One puzzle. Returns (score, lives, exited_early)."""
    puzzle = random.choice(_PUZZLES)
    diff_idx = random.randint(0, 15)
    invert = random.randint(0, 1)
    # Random starting cursor: occasionally lands on the answer for a
    # freebie, which feels lucky rather than buggy.
    cursor = random.randint(0, 15)

    _draw_grid(puzzle, diff_idx, invert)
    _draw_cursor(cursor, True)
    _update_header(score, lives)

    while True:
        kb.tick()
        intent = _key_intent(kb.get_key())
        if intent is not None:
            tag = intent[0]
            if tag == "exit":
                return score, lives, True
            if tag in ("left", "right", "up", "down"):
                _draw_cursor(cursor, False)
                cursor = _move_cursor(cursor, tag)
                _draw_cursor(cursor, True)
            elif tag == "pick":
                if cursor == diff_idx:
                    _flash_cell(cursor, _GREEN, _FLASH_MS)
                    return score + 1, lives, False
                _flash_cell(cursor, _RED, _FLASH_MS)
                lives -= 1
                _restore_cell(cursor, puzzle, diff_idx, invert)
                _draw_cursor(cursor, True)
                _update_header(score, lives)
                if lives <= 0:
                    return score, lives, False
        time.sleep_ms(40)


def _game_over(kb, score):
    """Blocking end-of-game screen. Returns 'restart' or 'exit'."""
    _LCD.fillRect(0, 21, _W, _H - 21 - 18, _BLACK)
    _LCD.setTextSize(2)
    _LCD.setTextColor(_ORANGE, _BLACK)
    t = "Game Over"
    _LCD.drawString(t, (_W - _LCD.textWidth(t)) // 2, 34)
    _LCD.setTextSize(1)
    _LCD.setTextColor(_CREAM, _BLACK)
    s = "score: {}".format(score)
    _LCD.drawString(s, (_W - _LCD.textWidth(s)) // 2, 68)
    _LCD.setTextColor(_GRAY_MID, _BLACK)
    h = "R again   ` exit"
    _LCD.drawString(h, (_W - _LCD.textWidth(h)) // 2, 92)

    while True:
        kb.tick()
        i = _key_intent(kb.get_key())
        if i is not None:
            if i[0] == "restart":
                return "restart"
            if i[0] == "exit":
                return "exit"
        time.sleep_ms(40)


def run():
    _set_font()
    kb = MatrixKeyboard()
    # Debounce the launcher keypress so it doesn't immediately pick
    # the random starting cell. Same 400 ms window the other apps use.
    time.sleep_ms(400)
    try:
        while True:
            score = 0
            lives = _START_LIVES
            _draw_chrome()
            while lives > 0:
                score, lives, exited = _play_puzzle(kb, score, lives)
                if exited:
                    return
            if _game_over(kb, score) == "exit":
                return
    finally:
        # Mirror snake / whack / claude_buddy: clear, brief pause,
        # soft-reset back to the launcher. UIFlow 2.0 has no
        # return-to-launcher API.
        try:
            _LCD.fillScreen(_BLACK)
        except Exception as e:
            print("spot_diff: clear warning:", e)
        time.sleep_ms(200)
        machine.reset()


# UIFlow's App List invokes apps both as __main__ and via import; the
# empirical behavior is always-run, so just call run() bare (same
# pattern as snake / whack / hello).
run()
